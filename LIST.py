# # 1.sort()method-sorting the list by assending order
lst=[1,5,7,3,9]
lst.sort()
print(lst)

# # 2.reverse()method-Reverse elements from the list
lst=[1,2,3,4,5]
lst.reverse()
print(lst)

# # 3.Append()-this method use to add single element in end of the list
lst=[1,2,3,4]
lst.append(5)
print(lst)

# # 4.Extend()-Add more than one elements in end of the list
lst=[1,2,3,4,5]
lst.extend([6,7,8])
print(lst)

# # 5.pop()-remove last element from the list
lst=[1,2,3,4,5]
lst.pop()
print(lst)

# # 6.Remove()-this method is use to remove any element from the list
lst=[1,2,3,4,5]
lst.remove(4)
print(lst)

